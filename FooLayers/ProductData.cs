        {
            BusinessLayer.FooEnterprises.Product obj = new BusinessLayer.FooEnterprises.Product(ProductId);
            byte[] myArray = _concurrencyArray;
            byte[] clsArray = GetSerializedObject(obj);

            if (myArray.Length != clsArray.Length)
